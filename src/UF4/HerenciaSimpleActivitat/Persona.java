package UF4.HerenciaSimpleActivitat;

public class Persona {

    private int id;
    private int edat;

    private String nom;

    private String cognom;

    private String adreça;
    public int getId() {
        return id;
    }

    public int getEdat() {
        return edat;
    }

    public String getNom() {
        return nom;
    }

    public String getCognom() {
        return cognom;
    }

    public String getAdreça() {
        return adreça;
    }

}
