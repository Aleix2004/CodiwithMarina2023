package UF4.HerenciaMultinivell;

public class Arona extends SEAT {

    private String color;

    private int km;

    public void setColor(String color) {
        this.color = color;
    }

    public void setKm(int km) {
        this.km = km;
    }

    public String getColor() {
        return color;
    }

    public int getKm() {
        return km;
    }

}
